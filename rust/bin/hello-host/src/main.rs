use wasmtime::{
    component::bindgen, component::Component, component::Linker, Config, Engine, Store,
};
use wasmtime_wasi::preview2::{ResourceTable, WasiCtx, WasiView};

bindgen!({
  path: "../../../wit",
  world: "hello",
});

struct Host {
    table: ResourceTable,
    ctx: wasmtime_wasi::preview2::WasiCtx,
}

impl WasiView for Host {
    fn table(&self) -> &ResourceTable {
        &self.table
    }

    fn table_mut(&mut self) -> &mut ResourceTable {
        &mut self.table
    }

    fn ctx(&self) -> &WasiCtx {
        &self.ctx
    }

    fn ctx_mut(&mut self) -> &mut WasiCtx {
        &mut self.ctx
    }
}

impl HelloImports for Host {
    fn get_name(&mut self) -> wasmtime::Result<String> {
        Ok(String::from("Rust"))
    }

    fn print(&mut self, msg: String) -> wasmtime::Result<()> {
        println!("{}", msg);
        Ok(())
    }
}

impl Hello {
    // NOTE: Need to self implement as async call is not yet generated by bindgen
    async fn call_hello_async<S: wasmtime::AsContextMut>(
        &self,
        mut store: S,
    ) -> wasmtime::Result<()>
    where
        <S as wasmtime::AsContext>::Data: std::marker::Send,
    {
        let callee = unsafe { wasmtime::component::TypedFunc::<(), ()>::new_unchecked(self.hello) };
        let () = callee.call_async(store.as_context_mut(), ()).await?;
        callee.post_return_async(store.as_context_mut()).await?;
        Ok(())
    }
}

#[tokio::main]
async fn main() -> wasmtime::Result<()> {
    let mut config = Config::new();
    config.wasm_component_model(true);
    // WASI preview2 requires async support
    config.async_support(true);

    let engine = Engine::new(&config)?;
    let component =
        Component::from_file(&engine, "../../../wasm/components/rust_hello_guest.wasm")?;

    let mut linker = Linker::new(&engine);
    Hello::add_to_linker(&mut linker, |state: &mut Host| state)?;
    wasmtime_wasi::preview2::command::add_to_linker(&mut linker)?;

    let ctx = wasmtime_wasi::preview2::WasiCtxBuilder::new()
        .inherit_stdio()
        .inherit_stderr()
        .inherit_stdout()
        .build();

    let mut store = Store::new(
        &engine,
        Host {
            table: ResourceTable::default(),
            ctx,
        },
    );
    let instance = linker.instantiate_async(&mut store, &component).await?;
    let bindings = Hello::new(&mut store, &instance)?;

    println!("Calling hello async");
    bindings.call_hello_async(store).await?;
    println!("Called hello async");

    Ok(())
}
